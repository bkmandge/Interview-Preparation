"""                     ASCII value of a char

In Python, the ord() function is used to get the ASCII value of a character.

char = "A" -> 65
char = "e" -> 101

T.C. O(1)
S.C. O(1)

"""

def ASCIIvalue(char):
    print(f"ASCII value of {char} is: {ord(char)}")


ASCIIvalue('A')    
ASCIIvalue('e')


